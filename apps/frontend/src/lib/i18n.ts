export type TranslationKey = keyof typeof dict;

export function translate(key: TranslationKey, lang: "fr" | "ur"): string {
  const entry = dict[key];
  if (!entry) return key;
  return lang === "ur" ? entry.ur : entry.fr;
}

export function useT() {
  const language = useLanguageStore((s) => s.language);
  return {
    lang: language,
    dir: language === "ur" ? ("rtl" as const) : ("ltr" as const),
    t: (key: TranslationKey) => translate(key, language),
  };
}
